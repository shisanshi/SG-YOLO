class Transconv(nn.Module):
    def __init__(self, c1, c2):
        super(Transconv, self).__init__()
        self.cv1 = Conv(c1, c2, 1, 1)
        self.cv2 = Conv(c2, c2, 3, 1)
        self.cv3 = Conv(c2, c2, 1, 1)
